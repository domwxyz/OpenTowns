package xaos.utils;

public final class CharDef {

    /**
     * The id of the character
     */
    public short id;
    /**
     * The x location on the sprite sheet
     */
    public short x;
    /**
     * The y location on the sprite sheet
     */
    public short y;
    /**
     * The width of the character image
     */
    public short width;
    /**
     * The height of the character image
     */
    public short height;
    /**
     * The yOffset location on the sprite sheet
     */
    public short yoffset;
    /**
     * The xadvance location on the sprite sheet
     */
    public short xadvance;
    /**
     * The x location on the sprite sheet
     */
    public float xTex;
    /**
     * The y location on the sprite sheet
     */
    public float yTex;
    /**
     * The width of the character image
     */
    public float widthTex;
    /**
     * The height of the character image
     */
    public float heightTex;
}
