    public static final int SPECIAL_INT_WATER = 2;
    public static final int SPECIAL_INT_WATER_INF = 3;
    public static final int SPECIAL_INT_LAVA_1 = 4;
    public static final int SPECIAL_INT_LAVA = 5;
    public static final int SPECIAL_INT_LAVA_INF = 6;

    private int terrainID;
    private int special; // Water, lava, ...

    public MapGeneratorItem() {
        terrainID = -1;
        special = SPECIAL_INT_NONE;
    }

    public int getTerrainID() {
        return terrainID;
    }

    public void setTerrainID(int terrainID) {
        this.terrainID = terrainID;
    }

    public int getSpecial() {
        return special;
    }

    public void setSpecial(int special) {
        this.special = special;
    }

    public boolean hasSpecial() {
        return getSpecial() != SPECIAL_INT_NONE;
    }

    public static int getSpecialInt(String sType) {
        if (sType == null) {
            return SPECIAL_INT_NONE;
        }

        if (sType.equalsIgnoreCase(SPECIAL_WATER_1)) {
            return SPECIAL_INT_WATER_1;
        } else if (sType.equalsIgnoreCase(SPECIAL_WATER)) {
            return SPECIAL_INT_WATER;
        } else if (sType.equalsIgnoreCase(SPECIAL_WATER_INF)) {
            return SPECIAL_INT_WATER_INF;
        } else if (sType.equalsIgnoreCase(SPECIAL_LAVA_1)) {
            return SPECIAL_INT_LAVA_1;
        } else if (sType.equalsIgnoreCase(SPECIAL_LAVA)) {
            return SPECIAL_INT_LAVA;
        } else if (sType.equalsIgnoreCase(SPECIAL_LAVA_INF)) {
            return SPECIAL_INT_LAVA_INF;
        } else {
            return SPECIAL_INT_NONE;
        }
    }
